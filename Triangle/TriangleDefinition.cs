namespace Triangle
{
    // 三角形
    public class TriangleDefinition
    {
        public int A { get; set; }
        public int B { get; set; }
        public int C { get; set; }

        public TriangleDefinition(int a, int b, int c)
        {
            A = a;
            B = b;
            C = c;
        }
    }
}
